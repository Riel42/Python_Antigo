#Podemos usar uma divisão inteira

div_int = 10//3
print(div_int)

# 10 = 3*3+1 - Isso é divisão inteira, por isso 10//3 é o mesmo que 3
# 3*3 é igual a nove, e o 1 é o resto, por isso que 10 = 3*3+1, ou seja, a
#divisão inteira encontra um valor próxima a ser multiplicado para dar tal valor.

#Para saber o resto, é só utilizar o símbolo de porcentagem!

div_int2 = 10%3
print (div_int2)

#Para potenciação, é o valor ** a potência
div_int3 = 10**2
#10**2 é o mesmo que 10²
print(div_int3)

#Também podemos utilizar parênteses nas contas.
