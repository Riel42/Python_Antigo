"""
Faça um Programa que leia três números e mostre o maior e o menor deles.
"""
x = float(input('Digite o 1º Valor'))
y = float(input('Digite o 2º Valor'))
z = float(input('Digite o 3º Valor'))

if x > y and z:
    print (x)

if y > x and z:
    print (y)

if z > y and x:
    print (z)

if x < y and z:
    print (x)

if y < x and z:
    print (y)

if z < y and x:
    print (z)

