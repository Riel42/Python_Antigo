#Nesta aula eu vou aprender sobre as variáveis no Python.

#O sinal de igual é o sinal de atribuição, ou seja, quando eu falo que uma
#variável = 10, eu estou atribuindo o número 10 a variável

#exemplo:
var_x = 10
print(var_x)

#irá imprimir na tela o valor 10, que é o valor da var_x

var_y = 5
print ('Max comeu',var_y,'maçãs!')

#No exemplo acima eu usei uma variável dentro de uma mensagem, da mesma forma
#como é utilizada com os números!

a = 1
b = 2
c = 5

print ('O valor (a) vezes o (b) mais o (c) é igual a.: ', a*b+c)

#Também podemos fazer contas com as variáveis.

x = 100
y = x
print (y)

x = y = var_x
print (x, y, var_x)
#Uma variável também pode ter o valor de outra variável.

var01, var02, var03 = 10, 15, 20
print (var01, var02, var03)



