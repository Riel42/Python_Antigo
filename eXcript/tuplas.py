'''
Tuplas: as informações são do mesmo tipo,
heterogêneas. Não obrigatóriamente, os
tipos serão distintos, mas as tuplas
não podem ser modificadas, alteradas
os removidas.
'''
t = tuple("abc",)
print(t) #Objeto tiplo tupla
b = 'aaa', 1, True
print(b) #Objeto tiplo tupla
print(type(b))
