'''
and     -> e
or      -> ou
not     -> negação
is      -> é
is not  -> não é
in      -> está contido
not in  -> não está contido

OBS: No or, se uma condição for verdadeira, todo o código contido será verdadeiro.
'''
print(2<4 and 2==4)
print(2>4 or 2<4)
print(not(True))#vai inverter o resultado
print(2 is 2)
print(2 is not 2)
print(type(2) is int)
