x = 1
x += 1 #significa que x reccebe mais 1
print(x)

x = 6
x -= 1 #significa que vai ser tirado 1 de x
print(x)

x = 3
x *= 2 #significa que x reccebe vezes 2
print(x)

x = 9
x /= 3 #significa que x divide ele por 3
print(x)

x = 15
x %= 3 #significa que x mostra o resto da divisão de 15/3
print(x)

