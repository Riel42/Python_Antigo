num1 = input('Digite o primeiro número: ')
num1 = int(num1)

num2 = input('Digite o segundo número: ')
num2 = int(num2)

if (num1==num2): #igual
    print('O número %i é igual de %i'%(num1, num2))
if (num1!=num2): #diferente
    print('O número %i é diferente de %i'%(num1, num2))
if (num1<num2): #menor
    print('O número %i é menor de %i'%(num1, num2))
if (num1>num2): #maior
    print('O número %i é maior de %i'%(num1, num2))
if (num1<=num2): #menor ou igual
    print('O número %i é menor ou igual de %i'%(num1, num2))
if (num1>=num2): #maior ou igual
    print('O número %i é maior ou igual de %i'%(num1, num2)) 
    
