lista_nomes = ['Fulano','Ciclano','Beltrano','Marcos','Miguel','Euclano']
lista_nomes.reverse()
print(lista_nomes)#Lê uma lista de trás para frente.
lista_nomes.reverse()
print(lista_nomes)
lista_nomes.sort()
print(lista_nomes)#Ordena uma lista, fazendo com que ela fique organizada alfabeticamente ascendente.
lista_nomes.sort(reverse=True)
print(lista_nomes)#Ordena uma lista, fazendo com que ela fique organizada alfabeticamente de trás para frente.

